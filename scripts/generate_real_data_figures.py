#!/usr/bin/env python3
"""Generate report figures for the two-layer MOT evaluation design.

The script intentionally uses only MOT-format txt files and optional video metadata.
It does not require a detector, ReID weights, or GPU, so the figures can be
regenerated by course reviewers on CPU.
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Iterable

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd
from matplotlib import font_manager
from matplotlib.patches import FancyBboxPatch, Rectangle


def configure_fonts() -> None:
    """Prefer a CJK font when available so Chinese labels render correctly."""
    candidates = [
        "/System/Library/Fonts/STHeiti Medium.ttc",
        "/System/Library/Fonts/PingFang.ttc",
        "/System/Library/Fonts/Hiragino Sans GB.ttc",
        "/Library/Fonts/Arial Unicode.ttf",
    ]
    for font_path in candidates:
        if Path(font_path).exists():
            font_manager.fontManager.addfont(font_path)
            name = font_manager.FontProperties(fname=font_path).get_name()
            plt.rcParams["font.family"] = name
            break
    plt.rcParams["axes.unicode_minus"] = False

COLUMNS = [
    "frame",
    "id",
    "left",
    "top",
    "width",
    "height",
    "score",
    "x",
    "y",
    "z",
]

STYLE = {
    "blue": "#2563EB",
    "cyan": "#06B6D4",
    "green": "#16A34A",
    "orange": "#F97316",
    "red": "#DC2626",
    "purple": "#7C3AED",
    "gray": "#64748B",
    "dark": "#0F172A",
    "light": "#F8FAFC",
}


def read_mot(path: Path) -> pd.DataFrame:
    df = pd.read_csv(path, header=None, names=COLUMNS)
    df = df.dropna(subset=["frame", "id", "left", "top", "width", "height"])
    df[["frame", "id"]] = df[["frame", "id"]].astype(int)
    for col in ["left", "top", "width", "height", "score"]:
        df[col] = pd.to_numeric(df[col], errors="coerce")
    df["cx"] = df["left"] + df["width"] / 2.0
    df["cy"] = df["top"] + df["height"] / 2.0
    return df


def savefig(path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    plt.savefig(path, dpi=220, bbox_inches="tight", facecolor="white")
    plt.close()
    print(f"Generated: {path}")


def add_card(ax, xy, wh, title, body, color):
    box = FancyBboxPatch(
        xy,
        wh[0],
        wh[1],
        boxstyle="round,pad=0.03,rounding_size=0.08",
        linewidth=1.5,
        edgecolor=color,
        facecolor="white",
    )
    ax.add_patch(box)
    ax.text(xy[0] + wh[0] / 2, xy[1] + wh[1] - 0.18, title,
            ha="center", va="top", fontsize=11.5, fontweight="bold", color=color, linespacing=1.15)
    ax.text(xy[0] + wh[0] / 2, xy[1] + 0.45, body,
            ha="center", va="bottom", fontsize=9.2, color=STYLE["dark"], linespacing=1.28)


