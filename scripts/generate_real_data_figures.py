    add_card(ax, (0.35, 4.2), (2.5, 1.65), "第一层\nMOT 基准",
             "MOT17 / MOT20 GT\n标准数据格式\n通用评测协议", STYLE["blue"])
    add_card(ax, (0.35, 1.35), (2.5, 1.65), "第二层\n自标注验证集",
             "Wuzhou_MidRoad\n2474 帧 / 117 个ID\n33846 个标注框", STYLE["green"])

    add_card(ax, (3.55, 4.2), (2.35, 1.65), "算法流程",
             "读取检测/跟踪结果\n轨迹数据关联\nReID 外观辅助", STYLE["purple"])
